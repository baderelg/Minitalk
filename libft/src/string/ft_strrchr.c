#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	const char	*last;

	last = NULL;
	if ((char)c == '\0')
		return ((char *)&str[ft_strlen(str)]);
	while (*str)
	{
		if (*str == (char)c)
			last = str;
		str++;
	}
	return ((char *)last);
}
